package com.chess.trainer.backend.model;

public class Line {
    private String name;
    private String color;

    public void setName(String name) {
        this.name = name;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getColor() {
        return this.color;
    }

    public String getName() {
        return this.name;
    }
}
